/// Build module-specific prompt with depth-aware instructions.
///
/// The prompt has two parts:
/// 1. **Header** — module metadata (name, path, files, architecture, exports, code snippets).
///    This is identical for all depth levels.
/// 2. **Instructions** — what sections to generate, how many code examples, and target length.
///    This varies by depth level.
fn build_module_prompt(
    module: &crate::analysis::ModuleAnalysis,
    depth: &DepthLevel,
) -> String {
    let header = build_prompt_header(module);
    let depth_desc = depth_description(depth);
    let instructions = build_depth_instructions(&module.name, depth);

    format!(
        "You are a code documentation expert. Analyze this module and generate {} context documentation.\n\n\
         {}\n\n\
         IMPORTANT: Generate markdown content directly. Do NOT wrap your response in code blocks.\n\n\
         {}",
        depth_desc, header, instructions
    )
}

/// Build the shared metadata header (identical for all depths).
fn build_prompt_header(module: &crate::analysis::ModuleAnalysis) -> String {
    let dependencies = module.architecture.dependencies.join(", ");
    let dependents = module.architecture.dependents.join(", ");
    let external_deps = module.architecture.external_deps.join(", ");

    let exports_str = if !module.symbols.exports.is_empty() {
        module.symbols.exports
            .iter()
            .map(|e| format!("- {} ({})", e.name, e.kind))
            .collect::<Vec<_>>()
            .join("\n")
    } else {
        "(No exports detected by AST analyzer)".to_string()
    };

    format!(
        "MODULE: {}\nPATH: {}\nFILES: {}\n\n\
         ARCHITECTURE:\n\
         - Dependencies: {}\n\
         - Dependents: {}\n\
         - External: {}\n\n\
         EXPORTS (detected by AST analysis):\n{}\n\n\
         CODE SNIPPETS:\n{}",
        module.name,
        module.path,
        module.file_count,
        if dependencies.is_empty() { "none" } else { &dependencies },
        if dependents.is_empty() { "none" } else { &dependents },
        if external_deps.is_empty() { "none" } else { &external_deps },
        exports_str,
        module.code_snippets,
    )
}

/// Human-readable depth description for the prompt preamble.
fn depth_description(depth: &DepthLevel) -> &'static str {
    match depth {
        DepthLevel::Minimal => "brief, concise",
        DepthLevel::Normal => "comprehensive",
        DepthLevel::Detailed => "detailed and thorough",
        DepthLevel::Expert => "expert-level, exhaustive",
    }
}

/// Build depth-specific generation instructions.
fn build_depth_instructions(module_name: &str, depth: &DepthLevel) -> String {
    match depth {
        DepthLevel::Minimal => build_minimal_instructions(module_name),
        DepthLevel::Normal => build_normal_instructions(module_name),
        DepthLevel::Detailed => build_detailed_instructions(module_name),
        DepthLevel::Expert => build_expert_instructions(module_name),
    }
}

