use std::time::Instant;

// =============================================================================
// Configuration
// =============================================================================

/// Configuration for batch context generation
#[derive(Debug, Clone)]
pub struct BatchGenerationConfig {
    /// Batch ID (generated by BatchManager)
    pub batch_id: String,

    /// Absolute path to project root
    pub project_path: PathBuf,

    /// Module IDs to generate (remaining, not all selected)
    pub module_ids: Vec<String>,

    /// Module paths (relative to project_path, parallel to module_ids)
    pub module_paths: Vec<String>,

    /// LLM model to use
    pub model: String,

    /// LLM provider
    pub provider: LlmProviderType,

    /// Depth level for context generation
    pub depth_level: DepthLevel,

    /// System prompt for context generation (resolved from prompt registry)
    pub system_prompt: String,

    /// Full analysis output (from module detection step)
    pub analysis: AnalysisOutput,

    /// Wizard configuration (for checkpoint)
    pub wizard_config: WizardConfig,

    /// Total selected modules (for global progress tracking)
    pub total_selected: usize,

    /// Previously completed count (from checkpoint)
    pub previously_completed: usize,
}

// =============================================================================
// Events
// =============================================================================

/// Progress event for a single module
#[derive(Debug, Clone)]
pub struct ProgressEvent {
    /// Batch ID
    pub batch_id: String,

    /// Current module index (global, including previously completed)
    pub current: usize,

    /// Total modules to generate
    pub total: usize,

    /// Module ID being processed
    pub module_id: String,

    /// Status: "running", "completed", "failed"
    pub status: String,

    /// Tokens used (if completed)
    pub tokens_used: usize,

    /// Error message (if failed)
    pub error: Option<String>,
}

/// Completion event for entire batch
#[derive(Debug, Clone)]
pub struct CompleteEvent {
    /// Batch ID
    pub batch_id: String,

    /// Number of modules completed successfully
    pub total_completed: usize,

    /// Number of modules that failed
    pub total_failed: usize,

    /// Total duration in milliseconds
    pub duration_ms: u64,
}

/// Result of batch generation
#[derive(Debug, Clone)]
pub struct BatchGenerationResult {
    /// Number of modules completed successfully
    pub completed: usize,

    /// Number of modules that failed
    pub failed: usize,

    /// Total duration in milliseconds
    pub duration_ms: u64,
}

