//! # Batch Context Generation
//!
//! Core business logic for generating contexts in batch.
//! This module contains ALL orchestration logic.
//!
//! ## Design:
//! - Receives configuration struct with all inputs
//! - Uses callback functions for progress/completion events
//! - Handles checkpoint save/load, pause/resume, rate limiting
//! - Clients (Desktop, API, CLI) provide callbacks for their specific needs
//!
//! ## Usage:
//! ```rust,ignore
//! use venore_core::wizard::batch_generation::*;
//!
//! let config = BatchGenerationConfig { ... };
//!
//! generate_batch_contexts(
//!     config,
//!     |event| { /* handle progress */ },
//!     |event| { /* handle completion */ },
//! ).await?;
//! ```

use crate::analysis::AnalysisOutput;
use crate::checkpoint::{CheckpointManager, WizardConfig};
