/// Generate contexts for a batch of modules (callback-based API)
///
/// This is a backward-compatible wrapper around `generate_batch_contexts_with_emitter`.
/// New code should prefer using `generate_batch_contexts_with_emitter` directly.
///
/// # Arguments
///
/// * `config` - Complete configuration for batch generation
/// * `llm_gateway` - LLM Gateway for API calls
/// * `on_progress` - Callback for progress events (called for each module)
/// * `on_complete` - Callback for completion event (called once at end)
///
/// # Returns
///
/// Returns `BatchGenerationResult` with statistics
pub async fn generate_batch_contexts<F1, F2>(
    config: BatchGenerationConfig,
    llm_gateway: Arc<LlmGateway>,
    on_progress: F1,
    on_complete: F2,
) -> Result<BatchGenerationResult>
where
    F1: Fn(ProgressEvent) + Send + Sync + 'static,
    F2: Fn(CompleteEvent) + Send + Sync + 'static,
{
    // Wrap callbacks in CallbackEventEmitter
    let emitter = Arc::new(CallbackEventEmitter::new(on_progress, on_complete));
    generate_batch_contexts_with_emitter(config, llm_gateway, emitter).await
}

/// Generate contexts for a batch of modules (event emitter-based API)
///
/// This function contains ALL orchestration logic:
/// - Checkpoint management (load/save)
/// - Pause/resume handling
/// - LLM calls with rate limiting
/// - File writing with frontmatter
/// - Event emission via WizardEventEmitter trait
///
/// # Arguments
///
/// * `config` - Complete configuration for batch generation
/// * `llm_gateway` - LLM Gateway for API calls
/// * `event_emitter` - Event emitter for progress/completion notifications
///
/// # Returns
///
/// Returns `BatchGenerationResult` with statistics
///
/// # Errors
///
/// Returns `VenoreError` if:
/// - Checkpoint operations fail critically
/// - All modules fail to generate
/// - Batch is paused and never resumed (timeout)
///
/// # Example
///
/// ```rust,ignore
/// use std::sync::Arc;
/// use venore_core::wizard::{WizardEventEmitter, NullEventEmitter};
///
/// let emitter: Arc<dyn WizardEventEmitter> = Arc::new(NullEventEmitter);
/// let result = generate_batch_contexts_with_emitter(config, gateway, emitter).await?;
/// ```
pub async fn generate_batch_contexts_with_emitter(
    config: BatchGenerationConfig,
    llm_gateway: Arc<LlmGateway>,
    event_emitter: Arc<dyn WizardEventEmitter>,
) -> Result<BatchGenerationResult>
{
    let start_time = Instant::now();
    let mut completed = 0;
    let mut failed = 0;

    tracing::info!(
        "🚀 Starting batch generation: {} remaining of {} total selected",
        config.module_ids.len(),
        config.total_selected
    );

    // 1. CHECKPOINT MANAGEMENT
    let checkpoint_manager = CheckpointManager::new(&config.project_path);

    match checkpoint_manager.load() {
        Ok(Some(_)) => {
            tracing::info!("📦 Loaded existing checkpoint (resuming)");
        }
        Ok(None) | Err(_) => {
            tracing::info!("📦 No checkpoint found, creating new one...");
            if let Err(e) = checkpoint_manager.initialize_with_wizard_config(
                config.project_path.clone(),
                config.wizard_config.clone(),
                config.total_selected,
            ) {
                tracing::error!("❌ Failed to initialize checkpoint: {}", e);
            } else {
                tracing::info!("✅ Checkpoint initialized");
            }
        }
    }

    // 2. GET BATCH STATE (for pause/resume)
    let batch_manager = BatchManager::global();
    let paused_flag = {
        let guard = batch_manager.lock().unwrap();
        guard.get_pause_flag(&config.batch_id)
            .ok_or_else(|| VenoreError::Unknown(
                format!("Batch {} not found in BatchManager", config.batch_id)
            ))?
    };

    // 3. GENERATE CONTEXTS FOR EACH MODULE
    for (i, module_id) in config.module_ids.iter().enumerate() {
        // 3.1. CHECK PAUSE FLAG (wait if paused)
        let is_paused = paused_flag.load(Ordering::Relaxed);
        if is_paused {
            tracing::info!("⏸️  Paused at module {}/{}, waiting...", i + 1, config.module_ids.len());
        }

        while paused_flag.load(Ordering::Relaxed) {
            tokio::time::sleep(tokio::time::Duration::from_millis(500)).await;
        }

        if is_paused {
            tracing::info!("▶️  Resumed from module {}/{}", i + 1, config.module_ids.len());
        }

        // 3.2. CALCULATE GLOBAL PROGRESS
        let current_global = config.previously_completed + i + 1;
        tracing::info!(
            "[{}/{}] Generating context for module: {}",
            current_global,
            config.total_selected,
            module_id
        );

        // 3.3. EMIT "RUNNING" PROGRESS
        event_emitter.emit_progress(ProgressEvent {
            batch_id: config.batch_id.clone(),
            current: current_global,
            total: config.total_selected,
            module_id: module_id.clone(),
            status: "running".to_string(),
            tokens_used: 0,
            error: None,
        });

        // 3.4. FIND MODULE ANALYSIS
        let module_path_str = &config.module_paths[i];
        let module_path = PathBuf::from(module_path_str);
        let module_name = module_path
            .file_name()
            .and_then(|n| n.to_str())
            .unwrap_or("unknown");

        let module_analysis = config.analysis.modules.iter()
            .find(|m| m.name == module_name);

        let module_analysis = match module_analysis {
            Some(m) => m,
            None => {
                tracing::error!("❌ Module '{}' not found in analysis", module_id);
                failed += 1;
                event_emitter.emit_progress(ProgressEvent {
                    batch_id: config.batch_id.clone(),
                    current: current_global,
                    total: config.total_selected,
                    module_id: module_id.clone(),
                    status: "failed".to_string(),
                    tokens_used: 0,
                    error: Some("Module not found in analysis".to_string()),
                });
                continue;
            }
        };

        // 3.5. BUILD PROMPT
        let prompt = build_module_prompt(module_analysis, &config.depth_level);

        // 3.6. CALL LLM
        let llm_request = LlmRequest {
            model: config.model.clone(),
            messages: vec![
                LlmMessage {
                    role: MessageRole::System,
                    content: config.system_prompt.clone(),
                    tool_call_id: None,
                    tool_calls: None,
                    content_parts: None,
                },
                LlmMessage {
                    role: MessageRole::User,
                    content: prompt,
                    tool_call_id: None,
                    tool_calls: None,
                    content_parts: None,
                },
            ],
            temperature: Some(0.3),
            max_tokens: Some(config.depth_level.max_tokens()),
            tools: None,
            json_schema: None,
            timeout_secs: Some(120),
            web_search: false,
        };

        let options = GatewayOptions::for_task(LlmTask::Onboarding)
            .with_provider(config.provider)
            .with_model(config.model.clone());

        match llm_gateway.complete(llm_request, options).await {
            Ok(response) => {
                let tokens = response.usage.as_ref().map(|u| u.total_tokens).unwrap_or(0);

                // 3.7. WRITE CONTEXT FILE
                let module_dir = config.project_path.join(module_path_str);
                let context_file = module_dir.join(".context.md");

                let metadata = build_metadata(
                    &module_analysis.name,
                    &config.model,
                    config.provider,
                    &response,
                    &module_analysis.code_snippets,
                );

                // Ensure directory exists
                if !module_dir.exists() {
                    tracing::warn!("Creating module directory: {}", module_dir.display());
                    let _ = std::fs::create_dir_all(&module_dir);
                }

                // Build content with frontmatter
                let frontmatter = match FrontmatterBuilder::build(&metadata) {
                    Ok(fm) => fm,
                    Err(e) => {
                        tracing::error!("Failed to build frontmatter: {}", e);
                        "---\n---\n".to_string()
                    }
                };

                let content = format!("{}\n\n{}", frontmatter, response.content);

                tracing::info!(
                    "Writing context file: {} (provider: {}, model: {})",
                    context_file.display(),
                    response.provider.as_str(),
                    response.model
                );

                let temp_file = context_file.with_extension("context.md.tmp");
                match std::fs::write(&temp_file, &content)
                    .and_then(|_| std::fs::rename(&temp_file, &context_file)) {
                    Ok(_) => {
                        completed += 1;
                        tracing::info!("✅ Generated context for {}: {} tokens", module_id, tokens);

                        // 3.8. UPDATE CHECKPOINT
                        if let Err(e) = checkpoint_manager.mark_completed(module_name.to_string()) {
                            tracing::error!("❌ Failed to update checkpoint: {}", e);
                        } else {
                            let total_completed_global = config.previously_completed + completed;
                            tracing::info!(
                                "✅ Checkpoint updated: {}/{} modules completed",
                                total_completed_global,
                                config.total_selected
                            );
                        }

                        // 3.9. EMIT "COMPLETED" PROGRESS
                        event_emitter.emit_progress(ProgressEvent {
                            batch_id: config.batch_id.clone(),
                            current: current_global,
                            total: config.total_selected,
                            module_id: module_id.clone(),
                            status: "completed".to_string(),
                            tokens_used: tokens as usize,
                            error: None,
                        });
                    }
                    Err(e) => {
                        failed += 1;
                        tracing::error!("❌ Failed to write context: {}", e);
                        event_emitter.emit_progress(ProgressEvent {
                            batch_id: config.batch_id.clone(),
                            current: current_global,
                            total: config.total_selected,
                            module_id: module_id.clone(),
                            status: "failed".to_string(),
                            tokens_used: 0,
                            error: Some(format!("Failed to write file: {}", e)),
                        });
                    }
                }
            }
            Err(e) => {
                failed += 1;
                tracing::error!("❌ LLM failed for {}: {}", module_id, e);
                event_emitter.emit_progress(ProgressEvent {
                    batch_id: config.batch_id.clone(),
                    current: current_global,
                    total: config.total_selected,
                    module_id: module_id.clone(),
                    status: "failed".to_string(),
                    tokens_used: 0,
                    error: Some(format!("LLM generation failed: {}", e)),
                });
            }
        }

        // 3.10. RATE LIMITING DELAY
        if i < config.module_ids.len() - 1 {
            let delay_secs = match config.provider {
                LlmProviderType::Gemini => 4,
                LlmProviderType::Anthropic => 2,
                LlmProviderType::OpenAI => 2,
                LlmProviderType::Ollama | LlmProviderType::Tavily => 0,
            };

            if delay_secs > 0 {
                tracing::info!("⏳ Waiting {}s before next module...", delay_secs);
                tokio::time::sleep(tokio::time::Duration::from_secs(delay_secs)).await;
            }

            // Check pause after delay
            let is_paused_after_delay = paused_flag.load(Ordering::Relaxed);
            if is_paused_after_delay {
                tracing::info!("⏸️  Paused after rate limit delay, waiting...");
            }

            while paused_flag.load(Ordering::Relaxed) {
                tokio::time::sleep(tokio::time::Duration::from_millis(500)).await;
            }

            if is_paused_after_delay {
                tracing::info!("▶️  Resumed after rate limit delay");
            }
        }
    }

    // 4. EMIT COMPLETION EVENT
    let duration_ms = start_time.elapsed().as_millis() as u64;

    event_emitter.emit_complete(CompleteEvent {
        batch_id: config.batch_id.clone(),
        total_completed: completed,
        total_failed: failed,
        duration_ms,
    });

    tracing::info!(
        "✅ Batch generation completed: {} succeeded, {} failed, {}ms",
        completed,
        failed,
        duration_ms
    );

    Ok(BatchGenerationResult {
        completed,
        failed,
        duration_ms,
    })
}

